int arith(int i)
{
	char s;
	if(i == 0)
	    s = '+';
	else if(i == 1 || i == 2)
	    s = '-';
	else if(i == 3)
	    s = '*';
	else if(i == 4 || i == 5)
	    s = '/';

	return s;
}


